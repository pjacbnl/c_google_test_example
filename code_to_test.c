#include <stdlib.h>

/**
 * @brief Add value1 to value2
 * @param value1 A value
 * @param value2 Another value
 * @return The result
 **/
int add(int value1, int value2) {
    return value1+value2;
}

/**
 * @brief Subtract value2 from value1
 * @param value1 A value
 * @param value2 Another value
 * @return The result
 **/
int sub(int value1, int value2) {
    return value1-value2;
}
